			<p class="red-text  lighten-3">
				Los murciélagos pueden transportar a un amplio espectro de enfermedades, y eso los hace incómodos para muchos seres humanos. La rabia, es a menudo transportada por los perros, sin embargo, los murciélagos también pueden llevarla y más personas son infectadas al año por murciélagos que por perros, un murciélago puede morder a un humano mientras duerme y la persona ni siquiera lo sentirá. Es por esto qué los niños son especialmente una preocupación cuando se trata de la rabia de los murciélagos.<br>
