					Son mamíferos que están distribuidos por todo el mundo, excepto en las regiones polares. Sólo tres especies de murciélago son hematófagas, es decir, se alimentan de la sangre de otros animales. Son los únicos mamíferos que pueden volar. Aportan beneficios ecológicos como polinizadores, también para dispersión de semillas, para la reproducción de algunas plantas tropicales e incluso como controladores de plagas .Su alimentación es muy variada dependiendo la especie;  pueden alimentarse de insectos, aves, anfibios, reptiles, peces, arañas y otros artrópodos, mamíferos, frutas, polen, néctar, hojas, carroña y sangre.<br>
