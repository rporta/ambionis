				<p class="grey-text text-darken-3 lighten-3" style="font-weight: bold;font-size: 20px;">
					Descripción y comportamiento
				</p>
				<p class="grey-text text-darken-3 lighten-3">
					Las arañas están presentes en todos los continentes exceptuando la Antártida. Son depredadoras y casi todas poseen glándulas venenosas que dependiendo la víctima, puede ser  inofensiva o letal. Hoy en día existen más de 42,000 especies de arañas en el mundo. No pertenecen a los insectos, por lo que no deben catalogarse como tales.<br>
					Pueden encontrarse ejemplares en esta especie en casi todos los rincones del planeta. Desde regiones costeras hasta zonas montañosas y desérticas. <span class="grey-text text-darken-3 lighten-3" style="font-weight: bold;">Habitan </span>en arbustos, árboles vivos o caídos, pastizales, debajo de las piedras, en madrigueras bajo la tierra e <span class="grey-text text-darken-3 lighten-3" style="font-weight: bold;">incluso interactúan y comparten espacios con el ser humano, pues muchas de ellas viven en esquinas o techos de paredes, cajas y otras construcciones hechas por el hombre.</span><br>
					Las arañas producen hilos de seda con las que tejen telarañas o redes de caza, donde sus presas quedan atrapadas sin poder despegarse. Hay diversidad en el tamaño de las arañas que pueden ir de menos de 0.37 mm a los 9 cm. Con las patas extendidas pueden alcanzar los 25 cm. Los machos suelen ser de menor tamaño que las hembras. En el apareamiento, el macho introduce un espermatóforo en los conductos sexuales con ayuda de sus pedipalpos. Al final de la cópula, lo más común es que la hembra acabe por devorar a su pareja. Las hembras ponen sacos con cientos de huevos cubiertos de seda. En algunas especies. Una vez que nacen, a lo largo de su vida van mudando capas exteriores antes de alcanzar su tamaño completo. Si alguna araña llegara a perder alguna extremidad de su cuerpo, esta puede regenerarse siempre y cuando sea joven y se encuentre dentro del período de muda. 
