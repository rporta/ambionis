				<p class="grey-text text-darken-3 lighten-3" style="font-weight: bold;font-size: 20px;">
					Descripción y comportamiento
				</p>
				<p class="grey-text text-darken-3 lighten-3">
Esta especie es considerada mundialmente como la más importante plaga de las maderas blandas elaboradas.<br><br>

La larva de este insecto es la que produce el mayor daño a través de las galerías elípticas que produce al seguir el sentido de las fibras de la madera.<br><br>
 
Las condiciones que se presentan en Argentina en las construcciones, en especial cuando se trata de techos de madera y tejas o chapas, favorecen su desarrollo y una gran actividad acentuada en los meses de verano. Debido a la humedad y temperatura ambiente en la que se encuentra la madera, se determina un hábitat ideal para el insecto. Los techos de madera constituyen para el insecto un micro-clima ideal.<br><br>
  
<span style="font-weight: bold;">La duración del ciclo compuesto es variable, y puede ir desde 3 a 12 años</span> dependiendo de la temperatura, e incluso, bajo circunstancias ambientales muy determinadas, la variación tan grande de ese margen corresponde básicamente al <span style="font-weight: bold;">estadío larval</span>. Las condiciones óptimas para su desarrollo son una humedad relativa elevada (menor del 30%) y una temperatura relativamente alta (entre 28 y 30°C)<br><br>
 
<span style="font-weight: bold;">La hembra deposita entre 140 y 200 huevos en tandas de 2 a 8 durante 12 días</span>. Con la ayuda del oviscapto largo y muy flexible, éstos logran ser depositados en las grietas y fisuras de la madera. Eclosionan aproximadamente a las dos semanas. Si las grietas fuesen más anchas el insecto entrará en las mismas ovipositando a mayor profundidad o lateralmente en ellas.<br><br>
 
<span style="font-weight: bold;">Anterior a su colocación en obra</span>. En este caso es frecuente recibir madera con los huevos de insectos o larvas ya nacidas sin que se adviertan en el exterior. <span style="font-weight: bold;">Vienen dentro de la madera desde el monte o se contagian en los corralones de madera</span>.<br><br>
 
<span style="font-weight: bold;">En madera ya instalada</span>. La vía de infestación son los insectos adultos provenientes del entorno que se aparean y depositan los huevos sobre giretas o canales de salida. Pueden ser insectos de otras construcciones o insectos que llegaron a la vivienda a través de leña.<br><br>
 
El Hylotrupes Bajulus (bicho taladro) adulto, es un insecto alado de 15 a 25 mm de longitud. Su cuerpo es alargado y de forma ligeramente aplanada de color negro o castaño gris oscuro. Se desplazan caminando muy ágilmente y volando. <span style="font-weight: bold;">No se alimentan ni hacen daño, sólo aseguran la perduración de su especie</span>. Las hembras tienen en el extremo abdominal un órgano especializado llamado “oviscapto” que utiliza para colocar los huevos dentro de grietas de la madera o de la corteza de los árboles. La vida del macho es corta, de 2 a 3 semanas. Las hembras pueden vivir unos 25 días y desplazarse unos 1000 metros.

